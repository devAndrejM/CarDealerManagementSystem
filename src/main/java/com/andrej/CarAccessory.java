package com.andrej;

public class CarAccessory {

    //Data Member
    private String Stereo;
    private String Modified;
    private String Customized;
    private String Leather;
    private String GPS;
    private String ElectricWindows;

    public String getStereo() {
        return Stereo;
    }

    public void setStereo(String stereo) {
        this.Stereo = stereo;
    }

    public String getModified() {
        return Modified;
    }

    public void setModified(String modified) {
        this.Modified = modified;
    }

    public String getCustomized() {
        return Customized;
    }

    public void setCustomized(String customized) {
        this.Customized = customized;
    }

    public String getLeather() {
        return Leather;
    }

    public void setLeather(String leather) {
        this.Leather = leather;
    }

    public String getGPS() {
        return GPS;
    }

    public void setGPS(String GPS) {
        this.GPS = GPS;
    }

    public String getElectricWindows() {
        return ElectricWindows;
    }

    public void setElectricWindows(String electricWindows) {
        this.ElectricWindows = electricWindows;
    }





    }
